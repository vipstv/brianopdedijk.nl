//add cards as soon as possible
window.addEventListener("DOMContentLoaded", () => {
  //as soon as the muziekjes div is loaded
  var muziekjes = document.getElementById("muziekjes");

  //add cards
  items.forEach((item) => {
    muziekjes.innerHTML += `
        <div class="col-lg-4 col-md-6 col-sm-12 mb-4">
            <div class="card" data-name="${
              item.name
            }" data-description="${item.description
      .replace(/\n/g, "<br>")
      //remove first newline
      .replace(/^<br>/, "")}" data-img="${item.img}"
  ${item.youtube ? `data-youtube="${item.youtube}"` : ""}
  ${item.spotify ? `data-spotify="${item.spotify}"` : ""}
  ${item.deezer ? `data-deezer="${item.deezer}"` : ""}
  ${item.apple ? `data-apple="${item.apple}"` : ""}
  ${item.soundcloud ? `data-soundcloud="${item.soundcloud}"` : ""}
   style="cursor: pointer" onclick="showModal(this)">
                <img class="card-img-top" src="${item.img}" alt="${
      item.name
    }" style="object-fit: contain;"/>
            </div>
        </div>
        `;
  });
});

async function showModal(elem) {
  //remove old modal
  var oldModal = document.getElementById("modal");
  if (oldModal) {
    oldModal.remove();
  }
  //get data from element
  var data = elem.dataset;
  //create modal, center on screen
  var modal = document.createElement("div");
  modal.classList.add("modal", "fade");
  modal.id = "modal";
  modal.tabIndex = "-1";
  modal.setAttribute("role", "dialog");
  modal.setAttribute("aria-labelledby", "modalLabel");
  modal.setAttribute("aria-hidden", "true");

  modal.style.display = "block";
  modal.style.position = "fixed";
  modal.style.top = "0";
  modal.style.left = "0";
  modal.style.width = "100%";
  modal.style.height = "100%";
  modal.style.overflow = "auto";
  modal.style.zIndex = "9999";
  modal.style.padding = "20px";
  modal.style.display = "none";
  modal.style.justifyContent = "center";
  modal.style.alignItems = "center";
  modal.style.flexDirection = "column";

  modal.innerHTML = `
    <div class="modal-dialog modal-lg" role="document"> 
        <div class="modal-content">
            <div class="modal-header">
                <h5 class="modal-title" id="modalLabel">${data.name}</h5>
                <button type="button" class="close" data-dismiss="modal" aria-label="Close" onclick="closeModal()">
                    <span aria-hidden="true">&times;</span>
                </button>
            </div>
            <div class="modal-body">
                ${data.description} 
            </div>
            <div class="modal-footer">
                ${
                  data.youtube
                    ? `<a href="${data.youtube}" target="_blank" class="btn btn-danger"><i class="fab fa-youtube"></i></a>`
                    : ""
                }
                ${
                  data.spotify
                    ? `<a href="${data.spotify}" target="_blank" class="btn btn-success"><i class="fab fa-spotify"></i></a>`
                    : ""
                }
                ${
                  data.deezer
                    ? `<a href="${data.deezer}" target="_blank" class="btn btn-primary"><i class="fab fa-deezer"></i></a>`
                    : ""
                }
                ${
                  data.apple
                    ? `<a href="${data.apple}" target="_blank" class="btn btn-dark"><i class="fab fa-apple"></i></a>`
                    : ""
                }
                ${
                  data.soundcloud
                    ? `<a href="${data.soundcloud}" target="_blank" class="btn btn-warning" style="background-color: #FE5501; color: white;"><i class="fab fa-soundcloud"></i></a>`
                    : ""
                }

                  </div>
        </div>
    </div>
    `;
  document.body.appendChild(modal);
  //show modal
  $("#modal").modal("show");
}
